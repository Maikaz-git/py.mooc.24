nr1 = int(input("Number 1:"))
nr2 = int(input("Number 2:"))
operation = input("Operation: ")

if operation == 'add':
    print(f"{nr1} + {nr2} = {nr1 + nr2}")

if operation == 'multiply':
    print(f"{nr1} * {nr2} = {nr1 * nr2}")

if operation == 'subtract':
     print(f"{nr1} - {nr2} = {nr1 - nr2}")